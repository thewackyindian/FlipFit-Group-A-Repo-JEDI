package com.flipkart.constants;

/**
 * Class to define ANSI color code constants for text formatting.
 * These constants can be used to format console output with different colors and styles.
 *
 * @author Navaratna
 */
public class ColorConstants {

    // Resets text formatting to default
    public static final String ANSI_RESET = "\u001B[0m";

    // ANSI code for black color text
    public static final String ANSI_BLACK = "\u001B[30m";

    // ANSI code for red color text
    public static final String ANSI_RED = "\u001B[31m";

    // ANSI code for green color text
    public static final String ANSI_GREEN = "\u001B[32m";

    // ANSI code for yellow color text
    public static final String ANSI_YELLOW = "\u001B[33m";

    // ANSI code for blue color text
    public static final String ANSI_BLUE = "\u001B[34m";

    // ANSI code for purple color text
    public static final String ANSI_PURPLE = "\u001B[35m";

    // ANSI code for cyan color text
    public static final String ANSI_CYAN = "\u001B[36m";

    // ANSI code for bold text formatting
    public static final String ANSI_BOLD = "\u001B[1m";
}